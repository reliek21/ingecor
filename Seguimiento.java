public class Seguimiento extends Servicios{
    private int numId;
    private String nombre;

    // Getter
    public int getNumId() {
        return numId;
    }

    public String getNombre() {
        return nombre;
    }

    // Setter
    public void setNumId(int newNumId) {
        this.numId = newNumId;
    }

    public void setNombre(String newNombre) {
        this.nombre = newNombre;
    }
}
